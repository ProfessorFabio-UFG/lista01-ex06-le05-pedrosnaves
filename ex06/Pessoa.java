public class Pessoa {
    private String nome;
    private String telefone;
    private String cpf;
    private int idade;
    private String sexo;

    public Pessoa(String nome, String telefone, String cpf, int idade, String sexo) {
        this.nome = nome;
        this.telefone = telefone;
        this.cpf = cpf;
        this.idade = idade;
        this.sexo = sexo;
    }
}
